public class PrimitiveTypes {

    public static void main(String[] args) {

        // Byte
        byte minByte = Byte.MIN_VALUE;
        byte maxByte = Byte.MAX_VALUE;
        System.out.println("Byte minimum value: " + minByte);
        System.out.println("Byte maximum value: " + maxByte);

        // Short
        short minShort = Short.MIN_VALUE;
        short maxShort = Short.MAX_VALUE;
        System.out.println("Short minimum value: " + minShort);
        System.out.println("Short maximum value: " + maxShort);

        // Int
        int minInt = Integer.MIN_VALUE;
        int maxInt = Integer.MAX_VALUE;
        System.out.println("Int minimum value: " + minInt);
        System.out.println("Int maximum value: " + maxInt);

        // Long
        long minLong = Long.MIN_VALUE;
        long maxLong = Long.MAX_VALUE;
        System.out.println("Long minimum value: " + minLong);
        System.out.println("Long maximum value: " + maxLong);

        // Float
        float minFloat = Float.MIN_VALUE;
        float maxFloat = Float.MAX_VALUE;
        System.out.println("Float minimum value: " + minFloat);
        System.out.println("Float maximum value: " + maxFloat);

        // Double
        double minDouble = Double.MIN_VALUE;
        double maxDouble = Double.MAX_VALUE;
        System.out.println("Double minimum value: " + minDouble);
        System.out.println("Double maximum value: " + maxDouble);

        // Char
        char minChar = Character.MIN_VALUE;
        char maxChar = Character.MAX_VALUE;
        System.out.println("Char minimum value: " + minChar);
        System.out.println("Char maximum value: " + maxChar);

        // Boolean
        boolean trueBool = true;
        boolean falseBool = false;
        System.out.println(trueBool);
        System.out.println(falseBool);

        // Examples declearing each primitive type
        byte randomByte = 5;
        short randomShort = 5;
        int randomInt = 5;
        Long randomLong = 5L;
        float randomFloat = 45.342f;
        double randomDouble = 45.342d;
        char randomChar = 'A';
        boolean randomBoolean = true;

    }
    
}